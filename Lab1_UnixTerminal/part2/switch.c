// Write a C program using a switch statement

#include <stdio.h>

int main(){

	int number = 2;


	switch(number) {

		case 1 :
			printf("number is 1! \n");
			break;
		case 2 :
			printf("number is 2! \n");
			break;
		case 3:
			printf("number is 3! \n");
			break;
		default :
			printf("It messed up... \n");

	}

	return 0;

}

